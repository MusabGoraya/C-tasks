//Odd or Even number
#include <iostream>
using namespace std;
int main (){
	int number;
	cout<<"Type a number please: ";
	cin>>number;
	if (number%2==0)
	cout<<number<< " is even number.";    //prints even number
	else 
	cout<<number<<" is odd number. ";    //prints odd number
	return 0;
	
	
	
}
